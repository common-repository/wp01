    <div class="collapse-title collapse-toggler">А) Через плагин UpdraftPlus WordPress Backup Plugin</div>
    <div class="collapse-body" style="display: none;">
        <p><strong>Резервное копирование сайта посредством плагина UpdraftPlus</strong></p>
        <p>Плагин UpdraftPlus относится к категории «shareware»: многие функции в бесплатной версии недоступны, однако,
            имеющихся возможностей вполне достаточно для создания резервных копий. Ключевым преимуществом данного
            расширения является возможность восстановления бэкапа непосредственно из интерфейса админки без
            использования сторонних скриптов или программ. Давайте создадим резервную копию с его помощью.</p>
        <p><strong>1. Установка и активация</strong></p>
        <p>1.1. Перейдите в панель управления сайтом, раздел «Плагины», и кликните по кнопке «Добавить новый».</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/1.png') ?>" alt="img" width="600"></p>
        <p>1.2. Введите ключевое слово «UpdraftPlus» в строку поиска и нажмите на «Установить» в карточке плагина.</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/2.png') ?>" alt="img" width="600"></p>
        <p>1.3. Дождитесь завершения установки, затем нажмите на «Активировать».</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/3.png') ?>" alt="img" width="600"></p>
        <p>1.4. Вы будете перенаправлены на страницу со списком установленных плагинов, где необходимо кликнуть по
            ссылке «Настройки», о чем подсказывает и сам плагин.</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/4.png') ?>" alt="img" width="600"></p>
        <p><strong>2. Создание резервной копии</strong></p>
        <p>2.1. UpdraftPlus не требует дополнительных настроек, поэтому для создания бэкапа можно сразу же нажать на
            кнопку «Создать РК сейчас».</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/5.png') ?>" alt="img" width="600"></p>
        <p>2.2. На следующем шаге можно выбрать опции резервной копии. Проставьте галочки в чекбоксах «Include the
            database in the backup», чтобы включить в архив базу данных, и «Include your files in the backup» для
            добавления файлов сайта.</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/6.png') ?>" alt="img" width="600"></p>
        <p>2.3. UpdraftPlus позволяет вручную указать таблицы базы данных (только в Premium-версии) и директории,
            которые необходимо добавить в бэкап. Чтобы это сделать, щелкните по «(...)» напротив соответствующего
            пункта. По умолчанию выбраны все имеющиеся папки в директории wp-content.</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/7.png') ?>" alt="img" width="600"></p>
        <p>2.4. Поставьте галочку в чекбокс «Only allow this backup deleted manually», если хотите дополнительно
            защитить резервную копию (данная опция исключает автоматическое удаление бэкапа), и нажмите на «Создать РК
            сейчас».</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/8.png') ?>" alt="img" width="600"></p>
        <p>2.5. Дождитесь окончания выполнения скрипта. Ни в коем случае не закрывайте страницу, иначе создание бэкапа
            прервется.</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/9.png') ?>" alt="img" width="600"></p>
        <p>2.6. Об успешной архивации просигнализирует всплывающее окно.</p>
        <p><img src="<?php WP01_Content::the_image_src('updraftplus/10.png') ?>" alt="img" width="600"></p>
        <p><strong>3. Загрузка резервной копии</strong></p>
        <p>Созданные резервные копии появляются в списке на главной странице плагина.</p>
