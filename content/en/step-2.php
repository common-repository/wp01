                обратиться в техническую поддержку хостинга;
            </li>
            <li>«Корневой каталог» — как следует из названия, дает возможность создать резервную копию корневого
                каталога, содержащую файлы принадлежащих вам сайтов;
            </li>
            <li>«Базы данных MySQL» — здесь можно выборочно скачать базу данных конкретного сайта, выбрав нужную из
                списка;
            </li>
            <li>«Серверы пересылки электронной почты и фильтры» — позволяет создать резервную копию ваших писем и
                настроек E-mail.
            </li>
